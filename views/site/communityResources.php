                            <h4 class="clr-b"><a name="NYS Medicaid Program">New York State Medicaid Program<a href="#CR"> &uarr; </a></h4>
                            <h6><a href='http://www.health.ny.gov/health_care/medicaid/index.htm'> www.health.ny.gov/health_care/medicaid/index.htm</a><br/>
                                    <br/>
                                    <h6 class="clr-b">You can apply to Medicaid if:</h6><br/>
                                    • You are a U.S. citizen (born in the U.S. or one of its territories) and provide a valid Social Security Number (SSN). A match with the Social Security Administration (SSA) will verify your SSN, date of birth/age and U.S citizenship. If SSA verifies this information, no further proof is needed. The SSA cannot verify birth information for a naturalized citizen. You will need proof of naturalization (e.g., Naturalization Certificate (N-550 or N-570) or a U.S. passport).<br/>
                                    • Proof of age (if not verified by SSA), like a birth certificate<br/>
                                    • Proof of citizenship or immigration status<br/>
                                    • Four weeks of recent paycheck stubs (if you are working)<br/>
                                    • Proof of your income from sources including Social Security, Supplemental Security Income (SSI), Veteran’s Benefits (VA), retirement, Unemployment Insurance Benefits (UIB), Child Support payments<br/>
                                    • If you or anyone who lives with you is 65 years or older, certified blind or certified disabled, you need to give information on bank accounts, insurance policies and other resources<br/>
                                    • Proof of where you live, like a rent receipt, landlord statement, mortgage statement or mailing envelope you received recently<br/>
                                    • Insurance benefit card or the policy (if you have any other health insurance)<br/>
                                    • Medicare Benefit Card (the red, white and blue card)<br/>
                                    If you would like assistance with completing a <a href='/web/files/doh-4220-1.pdf'>Medicaid application (form)</a>, please call Health Aide Inc. CDPAP Member Services at 347.620.6226<br/>
