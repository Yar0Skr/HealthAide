            <h4 class="clr-b"><a name="Advanced Directives">Advanced Directives<a href="#CR"> &uarr; </a></h4><br/>

            <h6>Advance directives are legal documents that ensure your requests are fulfilled in the event you cannot make decisions for yourself. Advance directives can come in the form of a Health Care Proxy, a Living Will or a Do Not Resuscitate (DNR) Order. These documents can instruct what care you wish to be given under certain circumstances, and/or they can authorize a particular family member or friend to make decisions on your behalf.<br/>
                <br/>
                It is your right to make advance directives as you wish. It is most important for you to document how you would like your care to continue if you are no longer able to communicate with providers in an informed way due to illness or injury. Please contact your Health Aide Inc. CDPAP Care Manager for assistance in completing these documents.</h6><br/>

