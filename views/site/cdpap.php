        <h3 class="text-center clr-g">Consumer Directed Personal Assistance Program (CDPAP)</h3>
        <h4 class="clr-b">• This Medicaid program provides services to chronically ill or physically disabled individuals who have a medical need for help with activities of daily living (ADLs) or skilled nursing services.</h4>
        <h4 class="clr-b">• Services can include any of the services provided by a personal care aide (home attendant), home health aide, or nurse.</h4>
        <h4 class="clr-b">• Recipients have flexibility and freedom in choosing their caregivers.</h4>
        <h4 class="clr-b">• The consumer or the person acting on the consumer's behalf (such as the parent of a disabled or chronically ill child) assumes full responsibility for hiring, training, supervising, and – if need be – terminating the employment of persons providing the services.</h4>
        <h3 class="text-center clr-g">How do I find the Consumer Directed Personal Assistance Program?</h3>
        <h4 class="clr-b">Before a person can receive services, his or her doctor must send a completed Physician's Order for Services to the local social services district, which then completes a social and nursing assessment. A nurse assessor then determines whether the recipient can appropriately participate in CDPAP, and recommends the amount, frequency and duration of services.</h4>
        <h3 class="text-center clr-g">Requirements of CDPAP</h3>
        <h4 class="clr-b">Recipients must be able and willing to make informed choices regarding the management of the services they receive, or have a legal guardian or designated relative or other adult able and willing to help make informed choices.</h4>
        <h4 class="clr-b">The consumer or designee must also be responsible for recruiting, hiring, training, supervising and terminating caregivers, and must arrange for back-up coverage when necessary, arrange and coordinate other services; and keep payroll records.</h4>
        <h4 class="clr-b">Click <a href=https://www.health.ny.gov/health_care/medicaid/#need> here</a> if you think you have an immediate need for Consumer Directed Personal Assistance Services and would like more information on how to get your eligibility processed more quickly for these services.</h4>
        <h4 class="clr-b">Additional Information</h4>
